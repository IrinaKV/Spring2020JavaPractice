package day12_Scanner2;

public class SwitchPractice {
    public static void main(String[] args) {
        switch(3){
            case 1 :
                System.out.println("Case1");
                break;
            case 2 :
                System.out.println("Case2");
                break;
            case 3 :
                System.out.println("Case3");
                break;
            default:
                System.out.println("Invalid Case");

        }
        System.out.println("==============================");



    }
}
