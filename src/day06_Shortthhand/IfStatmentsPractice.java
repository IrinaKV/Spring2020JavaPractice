package day06_Shortthhand;

public class IfStatmentsPractice {
    public static void main(String[] args) {
        int a = 100;
        int b = 100;
        if (a > b){
            System.out.println(a + " is greater than " +b);
        }
        if(b > a){
            System.out.println(b + " is greater than "+ a);
        }
        if(a==b){
            System.out.println(a + " is equal " + b);
        }
    }
}
